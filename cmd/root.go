package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "qsw-cli",
	Short: "Work smarter not harder",
	Long: `This CLI is build to help me during my journey as DevOps/SRE to be
more productive and more efficient in my work days 
but above all not thinking about standard thing that I already know`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

